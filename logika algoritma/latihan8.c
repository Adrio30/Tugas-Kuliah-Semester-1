#include <stdio.h>

int main() {
  // Deklarasi array integer dengan 5 elemen
  int totals[5];

  // Inisialisasi elemen array dengan nilai-nilai tertentu
  totals[0] = 10;
  totals[1] = 20;
  totals[2] = 30;
  totals[3] = 40;
  totals[4] = 50;

  // Tampilkan isi elemen ke-tiga
  printf("Isi elemen ke-3 dari array totals: %d\n", totals[2]);

  return 0;
}
